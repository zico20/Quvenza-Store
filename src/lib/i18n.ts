export function t(path: string, lang: Lang): string {
  const parts = path.split('.');
  let cur: unknown = STRINGS;
  for (const p of parts) {
    if (cur == null || typeof cur !== 'object') return path;
    cur = (cur as Record<string, unknown>)[p];
  }
  if (cur != null && typeof cur === 'object' && lang in (cur as object)) {
    return (cur as Record<string, string>)[lang];
  }
  return path;
}

// Server-side only — reads lang cookie from next/headers
export async function getServerLang(): Promise<Lang> {
  try {
    const { cookies } = await import('next/headers');
    const val = (await cookies()).get(LANG_COOKIE)?.value;
    return val === 'ar' ? 'ar' : DEFAULT_LANG;
  } catch {
    return DEFAULT_LANG;
  }
}
